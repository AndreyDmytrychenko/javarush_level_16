package com.lesson22.task2312.Snake;

public class Mouse {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Mouse(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
